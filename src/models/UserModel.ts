import mongoose, { Model, Schema } from 'mongoose';
import bcrypt from 'bcryptjs';

const userSchema: Schema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      required: true,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

const User: mongoose.Model<any> = mongoose.model('User', userSchema);

export default User;
